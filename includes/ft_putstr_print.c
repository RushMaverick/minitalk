#include "libft.h"

int	ft_putstr_print(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (write(1, "(null)", 6));
	while (*s)
	{
		write(1, s, 1);
		i++;
		s++;
	}
	return (i);
}
